function showDetail(title, key) {
    document.getElementById('modalTitle').innerText = title;
    
    // تحويل النص إلى HTML مع تحسينات العرض
    const content = contentData[key]
        .replace(/\n/g, '<br>')
        .replace(/═══════════════════════════/g, '<div class="divider-line"></div>')
        .replace(/✅/g, '<span style="color:#4CAF50; font-weight:bold;">✓ </span>')
        .replace(/❌/g, '<span style="color:#F44336; font-weight:bold;">✗ </span>')
        .replace(/🔴/g, '<span style="color:#F44336; font-weight:bold;">● </span>')
        .replace(/📱|📅|🔐|📋|🔄/g, '<span style="color:#2196F3; margin-left:5px;">$&</span>')
        .replace(/1️⃣|2️⃣|3️⃣|4️⃣|5️⃣|6️⃣|7️⃣|8️⃣|9️⃣|🔟/g, '<span style="color:#1976D2; font-weight:bold;">$&</span>');
    
    document.getElementById('modalBody').innerHTML = `<div class="modal-content-text">${content}</div>`;
    document.getElementById('detailModal').style.display = "block";
    
    // إيقاف تمرير الصفحة الرئيسية عند فتح النافذة
    document.body.style.overflow = 'hidden';
}

function closeModal() {
    document.getElementById('detailModal').style.display = "none";
    document.body.style.overflow = 'auto'; // إعادة تفعيل التمرير
}

// إغلاق النافذة عند الضغط خارجها
window.onclick = function(event) {
    let modal = document.getElementById('detailModal');
    if (event.target == modal) {
        closeModal();
    }
}

// إغلاق النافذة بمفتاح Escape
document.addEventListener('keydown', function(event) {
    if (event.key === 'Escape') {
        closeModal();
    }
});

function goBack() {
    if (window.history.length > 1) {
        window.history.back();
    } else {
        // إذا كانت الصفحة الرئيسية، يمكنك توجيهها إلى الصفحة الرئيسية
        window.location.href = 'index.html';
    }
}

// تحسينات لللمس على الجوال
document.addEventListener('DOMContentLoaded', function() {
    // منع التمرير المزدوج للتكبير
    let lastTouchEnd = 0;
    document.addEventListener('touchend', function(event) {
        const now = (new Date()).getTime();
        if (now - lastTouchEnd <= 300) {
            event.preventDefault();
        }
        lastTouchEnd = now;
    }, false);

    // تحسين استجابة الأزرار لللمس
    const buttons = document.querySelectorAll('button, .list-item');
    buttons.forEach(button => {
        button.addEventListener('touchstart', function() {
            this.style.transform = 'scale(0.98)';
        });
        
        button.addEventListener('touchend', function() {
            this.style.transform = 'scale(1)';
        });
    });

});




